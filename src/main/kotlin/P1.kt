fun main() {
    println("-------------------------Variables--------------------------------------")
    val integer: Int = 7
    println("Integer Value: $integer")
    val float = 1.5F
    println("Float Value: $float")
    val character: Char = 'x'
    println("Character Value: $character")
    val string: String = "Shubh Patel"
    println("String Value: $string")
    val bool: Boolean = true
    println("Boolean Value: $bool")
    val double: Double = 92.46
    println("Double Value: $double")
    val long: Long = 20012011127
    println("Long Value: $long")
    val short: Short = -4
    println("Short Value: $short")
    val byte: Byte = 127
    println("Byte Value: $byte")

}